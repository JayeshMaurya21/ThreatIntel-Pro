import puppeteer from "puppeteer";

export const runtime = "nodejs";

export async function POST(req: Request) {
  const data = await req.json();

  const { score, status, country, type, sources } = data;

  const browser = await puppeteer.launch({
    headless: true,
  });

  const page = await browser.newPage();

  // 🎨 Dynamic color
  let color = "green";
  if (status === "Suspicious") color = "orange";
  if (status === "Malicious") color = "red";

  // 🧠 Explanation (simple logic)
  const explanation = `
    This indicator has been analyzed using multiple threat intelligence sources.
    ${
      status === "Safe"
        ? "No significant malicious activity has been detected."
        : status === "Suspicious"
        ? "Some suspicious indicators were found. Further investigation is recommended."
        : "This indicator is highly likely to be malicious and should be blocked immediately."
    }
  `;

  await page.setContent(`
    <html>
      <head>
        <style>
          body { font-family: Arial; padding: 20px; }
          h1 { color: #333; }
          .status { font-size: 20px; font-weight: bold; color: ${color}; }
          .section { margin-top: 20px; }
        </style>
      </head>

      <body>
        <h1> ThreatIntel Report</h1>

        <div class="section">
          <p><strong>Type:</strong> ${type}</p>
          <p><strong>Country:</strong> ${country}</p>
          <p><strong>Score:</strong> ${score}</p>
          <p class="status">Status: ${status}</p>
        </div>

        <div class="section">
          <h3>Threat Intelligence Sources</h3>
          <p>VirusTotal Detections: ${sources?.vtMalicious || 0}</p>
          <p>AbuseIPDB Score: ${sources?.abuseScore || 0}</p>
          <p>OTX Pulses: ${sources?.otxPulses || 0}</p>
        </div>

        <div class="section">
          <h3>Analysis Summary</h3>
          <p>${explanation}</p>
        </div>

        <div class="section">
          <h3>Recommended Actions</h3>
          <ul>
            <li>Block the indicator if malicious</li>
            <li>Monitor related traffic</li>
            <li>Perform endpoint investigation</li>
          </ul>
        </div>

        <p style="margin-top:40px;">Generated by ThreatIntel</p>
      </body>
    </html>
  `);

  const pdf = await page.pdf({ format: "A4" });

  await browser.close();

  return new Response(pdf as unknown as BodyInit, {
    headers: {
      "Content-Type": "application/pdf",
      "Content-Disposition": "attachment; filename=report.pdf",
    },
  });
}